package com.partygallery.ui.theme

import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

/**
 * Party Gallery Design System - Spacing
 *
 * Consistent spacing values based on an 8dp grid system.
 * Provides visual rhythm and hierarchy throughout the app.
 */
object PartyGallerySpacing {

    // ============================================
    // BASE SPACING SCALE (8dp grid)
    // ============================================

    /** 0dp - No spacing */
    val none: Dp = 0.dp

    /** 4dp - Tight spacing */
    val xxs: Dp = 4.dp

    /** 8dp - Base unit */
    val xs: Dp = 8.dp

    /** 12dp - Small spacing */
    val sm: Dp = 12.dp

    /** 16dp - Default spacing */
    val md: Dp = 16.dp

    /** 20dp - Medium-large spacing */
    val lg: Dp = 20.dp

    /** 24dp - Large spacing */
    val xl: Dp = 24.dp

    /** 32dp - Extra large spacing */
    val xxl: Dp = 32.dp

    /** 40dp - Huge spacing */
    val xxxl: Dp = 40.dp

    /** 48dp - Section spacing */
    val section: Dp = 48.dp

    /** 64dp - Large section spacing */
    val sectionLarge: Dp = 64.dp

    // ============================================
    // COMPONENT-SPECIFIC SPACING
    // ============================================

    /** Screen horizontal padding */
    val screenHorizontal: Dp = 16.dp

    /** Screen vertical padding */
    val screenVertical: Dp = 16.dp

    /** Card internal padding */
    val cardPadding: Dp = 16.dp

    /** List item padding */
    val listItemPadding: Dp = 16.dp

    /** List item spacing */
    val listItemSpacing: Dp = 8.dp

    /** Button padding horizontal */
    val buttonPaddingHorizontal: Dp = 24.dp

    /** Button padding vertical */
    val buttonPaddingVertical: Dp = 12.dp

    /** Icon button padding */
    val iconButtonPadding: Dp = 12.dp

    /** Chip padding horizontal */
    val chipPaddingHorizontal: Dp = 12.dp

    /** Chip padding vertical */
    val chipPaddingVertical: Dp = 6.dp

    /** Input field padding */
    val inputPadding: Dp = 16.dp

    /** Dialog padding */
    val dialogPadding: Dp = 24.dp

    /** Bottom sheet padding */
    val bottomSheetPadding: Dp = 24.dp

    /** Navigation bar height */
    val navBarHeight: Dp = 80.dp

    /** App bar height */
    val appBarHeight: Dp = 64.dp

    /** Tab bar height */
    val tabBarHeight: Dp = 48.dp

    // ============================================
    // GRID & GALLERY SPACING
    // ============================================

    /** Media grid spacing */
    val gridSpacing: Dp = 2.dp

    /** Gallery grid spacing */
    val gallerySpacing: Dp = 4.dp

    /** Story carousel spacing */
    val storySpacing: Dp = 12.dp

    /** Feed item spacing */
    val feedItemSpacing: Dp = 16.dp
}
